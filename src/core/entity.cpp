std::string conv_entity(char32_t c) {
  char b = c & 0xff;

  if (c < 0x20) /* C0 */
    return " ";
  if (c < 0x7f) /* ASCII */
    return std::string(&b, 1);
  if (c < 0xa0) /* DEL, C1 */
    return " ";
  if (c == 0xa0)
    return NBSP;
  if (c == 0xad) /* SOFT HYPHEN */
    return "";
  if (c < 0x100) { /* Latin1 (ISO 8859-1) */
    if (UseAltEntity)
      return alt_latin1[c - 0xa0];
    return std::string(&b, 1);
  }
  if (c == 0x201c || c == 0x201f || c == 0x201d || c == 0x2033)
    return "\"";
  if (c == 0x2018 || c == 0x201b || c == 0x2019 || c == 0x2032)
    return "'";
  if (c >= 0x2010 && c < 0x2014)
    return "-";
  if (c == 0x2014)
    return "--";

  auto utf8 = codepoint_to_utf8(c);
  return std::string(utf8.data(), utf8.size());
}

