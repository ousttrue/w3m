char32_t getescapechar(const char **str) {
  const char *p = *str;
  if (*p == '&') {
    p++;
  }

  if (*p == '#') {
    p++;
    if (*p == 'x' || *p == 'X') {
      p++;
      if (!IS_XDIGIT(*p)) {
        *str = p;
        return -1;
      }
      char32_t dummy = -1;
      for (dummy = GET_MYCDIGIT(*p), p++; IS_XDIGIT(*p); p++)
        dummy = dummy * 0x10 + GET_MYCDIGIT(*p);
      if (*p == ';')
        p++;
      *str = p;
      return dummy;
    } else {
      if (!IS_DIGIT(*p)) {
        *str = p;
        return -1;
      }
      char32_t dummy = -1;
      for (dummy = GET_MYCDIGIT(*p), p++; IS_DIGIT(*p); p++)
        dummy = dummy * 10 + GET_MYCDIGIT(*p);
      if (*p == ';')
        p++;
      *str = p;
      return dummy;
    }
  }
  if (!IS_ALPHA(*p)) {
    *str = p;
    return -1;
  }

  auto q = p;
  for (p++; IS_ALNUM(*p); p++) {
    ;
  }

  std::string qq(q, p - q);
  auto strict_entity = true;
  if (strcasestr("lt gt amp quot apos nbsp", q) && *p != '=') {
    /* a character entity MUST be terminated with ";". However,
     * there's MANY web pages which uses &lt , &gt or something
     * like them as &lt;, &gt;, etc. Therefore, we treat the most
     * popular character entities (including &#xxxx;) without
     * the last ";" as character entities. If the trailing character
     * is "=", it must be a part of query in an URL. So &lt=, &gt=, etc.
     * are not regarded as character entities.
     */
    strict_entity = false;
  }
  if (*p == ';') {
    p++;
  } else if (strict_entity) {
    *str = p;
    return -1;
  }
  *str = p;

  auto found = g_entity_map.find(qq);
  if (found == g_entity_map.end()) {
    return -1;
  }
  return found->second;
}

std::string getescapecmd(const char **s) {
  const char *save = *s;

  int ch = getescapechar(s);
  if (ch >= 0) {
    return conv_entity(ch);
  }

  std::stringstream tmp;
  if (*save != '&')
    tmp << "&";
  tmp << save << (*s - save);
  return tmp.str();
}

std::tuple<std::string_view, std::string_view>
matchNamedCharacterReference(std::string_view src) {
  auto pos = src.find(';');
  if (pos != std::string::npos) {
    auto end = src.begin() + pos;
    auto found = g_entity_map.find(std::string(src.begin(), end));
    if (found != g_entity_map.end()) {
      return {{src.begin(), end + 1}, {end + 1, src.end()}};
    }
  }
  // not match
  return {{}, src};
}
